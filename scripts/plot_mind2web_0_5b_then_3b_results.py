#!/usr/bin/env python3
"""Plot a Mind2Web metric as 0.5B training followed by offset 3B training.

The input CSV is the one written by ``plot_mind2web_checkpoint_results.py``.
For the continuation experiment, each original 3B step ``x`` is plotted at
``prefix_end_step + x``. With the defaults, 3B step 100 is plotted at 3010.
"""

from __future__ import annotations

import argparse
import csv
import os
import re
import sys
from pathlib import Path


DEFAULT_CSV = Path("plots/mind2web_checkpoint_results/mind2web_checkpoint_results.csv")
DEFAULT_OUT_DIR = Path("plots/mind2web_checkpoint_results")
DEFAULT_PREFIX_EXPERIMENT = "mind2web_hyperclovax_0_5B_lora_ep6_lr1e_4_r64"
DEFAULT_CONTINUATION_EXPERIMENT = "mind2web_hyperclovax_3B_lora_ep6_lr1e_4_r64"
DEFAULT_OURS_EXPERIMENT = (
    "mind2web_hyperclovax_3B_lora_gradtransfer_normalize_scale1_ep6_lr1e_4_r64"
)
DEFAULT_CONTINUATION_EXPERIMENTS = [
    DEFAULT_CONTINUATION_EXPERIMENT,
    DEFAULT_OURS_EXPERIMENT,
]
DEFAULT_CONTINUATION_STEP_LIMITS = {
    DEFAULT_CONTINUATION_EXPERIMENT: 1400,
    DEFAULT_OURS_EXPERIMENT: 600,
}
DEFAULT_REFERENCE_EXPERIMENT = DEFAULT_OURS_EXPERIMENT
DEFAULT_REFERENCE_STEP = 600


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Plot 0.5B checkpoint results up to --prefix-end-step, then plot "
            "3B checkpoint results at prefix_end_step + source_step."
        )
    )
    parser.add_argument(
        "--csv",
        type=Path,
        default=DEFAULT_CSV,
        help="CSV generated by scripts/plot_mind2web_checkpoint_results.py.",
    )
    parser.add_argument(
        "--metric",
        default="exact_action_acc",
        help="Metric column to plot.",
    )
    parser.add_argument(
        "--prefix-experiment",
        default=DEFAULT_PREFIX_EXPERIMENT,
        help="Experiment used for the first part of the curve.",
    )
    parser.add_argument(
        "--continuation-experiments",
        nargs="+",
        default=DEFAULT_CONTINUATION_EXPERIMENTS,
        help=(
            "Experiment(s) plotted after --prefix-end-step. Each source step x "
            "is plotted at prefix_end_step + x."
        ),
    )
    parser.add_argument(
        "--prefix-end-step",
        type=int,
        default=2910,
        help="Last step of the prefix experiment and offset for continuation steps.",
    )
    parser.add_argument(
        "--out-dir",
        type=Path,
        default=DEFAULT_OUT_DIR,
        help="Directory where the plot and transformed CSV are saved.",
    )
    parser.add_argument(
        "--out-name",
        default=None,
        help="Output PNG filename. Defaults to '<metric>_0_5b_then_3b_by_step.png'.",
    )
    parser.add_argument(
        "--title",
        default=None,
        help="Custom plot title.",
    )
    parser.add_argument(
        "--y-min",
        type=float,
        default=None,
        help="Optional y-axis lower bound.",
    )
    parser.add_argument(
        "--y-max",
        type=float,
        default=None,
        help="Optional y-axis upper bound.",
    )
    parser.add_argument(
        "--show",
        action="store_true",
        help="Open an interactive matplotlib window after saving.",
    )
    parser.add_argument(
        "--connect-boundary",
        action="store_true",
        help="Draw a faint dashed line from the final 0.5B point to each first 3B point.",
    )
    return parser.parse_args()


def metric_title(metric: str) -> str:
    return metric.replace("_", " ").title()


def safe_filename(name: str) -> str:
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", name).strip("_")


def short_label(experiment: str) -> str:
    if experiment == DEFAULT_PREFIX_EXPERIMENT:
        return "0.5B"
    if experiment == DEFAULT_CONTINUATION_EXPERIMENT:
        return "3B"
    if "gradtransfer" in experiment:
        return "3B Ours"

    label = experiment
    label = label.replace("mind2web_hyperclovax_", "")
    label = label.replace("_lora_ep6_lr1e_4_r64", "")
    label = label.replace("_lora_", "_")
    return label


def continuation_step_limit(experiment: str) -> int | None:
    if experiment in DEFAULT_CONTINUATION_STEP_LIMITS:
        return DEFAULT_CONTINUATION_STEP_LIMITS[experiment]
    if "gradtransfer" in experiment:
        return 600
    return None


def read_rows(csv_path: Path) -> list[dict[str, str]]:
    if not csv_path.exists():
        raise SystemExit(f"CSV not found: {csv_path}")

    with csv_path.open("r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        rows = list(reader)

    if not rows:
        raise SystemExit(f"CSV is empty: {csv_path}")
    return rows


def available_experiments(rows: list[dict[str, str]]) -> list[str]:
    return sorted({row["experiment"] for row in rows if row.get("experiment")})


def select_points(
    rows: list[dict[str, str]],
    experiment: str,
    metric: str,
    *,
    max_step: int | None = None,
) -> list[tuple[int, float]]:
    points: list[tuple[int, float]] = []
    for row in rows:
        if row.get("experiment") != experiment:
            continue
        if metric not in row or row[metric] == "":
            continue
        step = int(row["step"])
        if max_step is not None and step > max_step:
            continue
        points.append((step, float(row[metric])))
    return sorted(points)


def require_points(
    rows: list[dict[str, str]],
    experiment: str,
    metric: str,
    *,
    max_step: int | None = None,
) -> list[tuple[int, float]]:
    points = select_points(rows, experiment, metric, max_step=max_step)
    if points:
        return points

    experiments = "\n  ".join(available_experiments(rows))
    raise SystemExit(
        f"No points found for experiment={experiment!r}, metric={metric!r}.\n"
        f"Available experiments:\n  {experiments}"
    )


def write_transformed_csv(
    path: Path,
    metric: str,
    prefix_experiment: str,
    prefix_points: list[tuple[int, float]],
    continuation_series: list[tuple[str, list[tuple[int, float]]]],
    prefix_end_step: int,
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "curve",
                "phase",
                "source_experiment",
                "source_step",
                "plot_step",
                metric,
            ],
        )
        writer.writeheader()
        for continuation_experiment, continuation_points in continuation_series:
            curve = f"{prefix_experiment} -> {continuation_experiment}"
            for source_step, value in prefix_points:
                writer.writerow(
                    {
                        "curve": curve,
                        "phase": "0.5B",
                        "source_experiment": prefix_experiment,
                        "source_step": source_step,
                        "plot_step": source_step,
                        metric: value,
                    }
                )
            for source_step, value in continuation_points:
                writer.writerow(
                    {
                        "curve": curve,
                        "phase": "3B",
                        "source_experiment": continuation_experiment,
                        "source_step": source_step,
                        "plot_step": prefix_end_step + source_step,
                        metric: value,
                    }
                )


def plot(
    output_path: Path,
    metric: str,
    prefix_experiment: str,
    prefix_points: list[tuple[int, float]],
    continuation_series: list[tuple[str, list[tuple[int, float]]]],
    prefix_end_step: int,
    reference_line: tuple[str, int, float] | None,
    title: str | None,
    y_min: float | None,
    y_max: float | None,
    show: bool,
    connect_boundary: bool,
) -> None:
    os.environ.setdefault("XDG_CACHE_HOME", "/tmp")
    os.environ.setdefault("MPLCONFIGDIR", "/tmp/matplotlib")
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise SystemExit("matplotlib is required to draw the plot.") from exc

    plt.rcParams.update(
        {
            "font.size": 18,
            "axes.titlesize": 24,
            "axes.labelsize": 21,
            "xtick.labelsize": 18,
            "ytick.labelsize": 18,
            "legend.fontsize": 16,
        }
    )

    fig, ax = plt.subplots(figsize=(13.5, 8))

    ax.plot(
        [step for step, _ in prefix_points],
        [value for _, value in prefix_points],
        marker="o",
        linewidth=2.8,
        markersize=7,
        label=short_label(prefix_experiment),
    )

    colors = plt.rcParams["axes.prop_cycle"].by_key().get("color", [])
    final_prefix_step, final_prefix_value = prefix_points[-1]
    for index, (continuation_experiment, continuation_points) in enumerate(continuation_series):
        xs = [prefix_end_step + step for step, _ in continuation_points]
        ys = [value for _, value in continuation_points]
        color = colors[(index + 1) % len(colors)] if colors else None
        ax.plot(
            xs,
            ys,
            marker="o",
            linewidth=2.8,
            markersize=7,
            color=color,
            label=short_label(continuation_experiment),
        )
        if connect_boundary and continuation_points:
            first_step, first_value = continuation_points[0]
            ax.plot(
                [final_prefix_step, prefix_end_step + first_step],
                [final_prefix_value, first_value],
                color=color,
                linestyle=":",
                linewidth=1,
                alpha=0.6,
            )

    if reference_line is not None:
        experiment, step, value = reference_line
        ax.axhline(
            value,
            color="0.2",
            linestyle="-.",
            linewidth=2.1,
            alpha=0.75,
            label=f"{short_label(experiment)} @ {step}: {value:.3f}",
        )

    ax.axvline(
        prefix_end_step,
        color="0.35",
        linestyle="--",
        linewidth=1.8,
        alpha=0.8,
        label=f"3B offset starts at {prefix_end_step}",
    )
    ax.set_xlabel(f"Step (0.5B step, then {prefix_end_step} + 3B step)")
    ax.set_ylabel(metric_title(metric))
    ax.set_title(title or f"{metric_title(metric)}: 0.5B then 3B")
    if y_min is not None or y_max is not None:
        ax.set_ylim(bottom=y_min, top=y_max)
    ax.grid(True, alpha=0.3)
    ax.legend(loc="lower right", frameon=True, framealpha=0.92)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    fig.tight_layout()
    fig.savefig(output_path, dpi=200, bbox_inches="tight")
    if show:
        plt.show()
    plt.close(fig)


def main() -> int:
    args = parse_args()
    rows = read_rows(args.csv)

    prefix_points = require_points(
        rows,
        args.prefix_experiment,
        args.metric,
        max_step=args.prefix_end_step,
    )
    continuation_series = [
        (
            experiment,
            require_points(
                rows,
                experiment,
                args.metric,
                max_step=continuation_step_limit(experiment),
            ),
        )
        for experiment in args.continuation_experiments
    ]
    reference_points = select_points(
        rows,
        DEFAULT_REFERENCE_EXPERIMENT,
        args.metric,
        max_step=DEFAULT_REFERENCE_STEP,
    )
    reference_line = next(
        (
            (DEFAULT_REFERENCE_EXPERIMENT, step, value)
            for step, value in reference_points
            if step == DEFAULT_REFERENCE_STEP
        ),
        None,
    )

    out_name = args.out_name or f"{safe_filename(args.metric)}_0_5b_then_3b_by_step.png"
    output_path = args.out_dir / out_name
    points_csv_path = output_path.with_suffix(".csv")

    plot(
        output_path=output_path,
        metric=args.metric,
        prefix_experiment=args.prefix_experiment,
        prefix_points=prefix_points,
        continuation_series=continuation_series,
        prefix_end_step=args.prefix_end_step,
        reference_line=reference_line,
        title=args.title,
        y_min=args.y_min,
        y_max=args.y_max,
        show=args.show,
        connect_boundary=args.connect_boundary,
    )
    write_transformed_csv(
        points_csv_path,
        args.metric,
        args.prefix_experiment,
        prefix_points,
        continuation_series,
        args.prefix_end_step,
    )

    print(f"Saved plot: {output_path}")
    print(f"Saved transformed CSV: {points_csv_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
